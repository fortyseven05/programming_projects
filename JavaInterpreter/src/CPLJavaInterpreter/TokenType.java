package CPLJavaInterpreter;

public enum TokenType {

    ASSIGN_TOK,             // =
    ID_TOK,                 // ID
    LE_TOK,                 // <=
    LT_TOK,                 // <
    GE_TOK,                 // >=
    GT_TOK,                 // >
    EQ_TOK,                 // ==
    NE_TOK,                 // !=
    ADD_TOK,                // +
    SUB_TOK,                // -
    MUL_TOK,                // *
    DIV_TOK,                // /
    MOD_TOK,                // %
    REV_DIV_TOK,            // \
    EXP_TOK,                // ^
    OPEN_PAREN_TOK,         // (
    CLOSE_PAREN_TOK,        // )
    ITER_TOK,              // :
    CONST_TOK,
    EOS_TOK,
    FUNC_KW_TOK,            // function
    PRINT_KW_TOK,           // print
    END_KW_TOK,             // end
    IF_KW_TOK,              // if
    ELSE_KW_TOK,            // else
    WHILE_KW_TOK,           // while
    FOR_KW_TOK,             // for




}

